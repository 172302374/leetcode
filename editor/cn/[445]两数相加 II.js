/**
 * Definition for singly-linked list.
 * function ListNode(val) {
 *     this.val = val;
 *     this.next = null;
 * }
 */
/**
 * @param {ListNode} l1
 * @param {ListNode} l2
 * @return {ListNode}
 */
var addTwoNumbers = function (l1, l2) {
    let arr1 = [];
    let arr2 = [];
    while (l1) {
        arr1.push(l1.val);
        l1 = l1.next;
    }
    while (l2) {
        arr2.push(l2.val);
        l2 = l2.next;
    }
    let ans = null;
    let carry = 0;
    while (!!arr1.length || !!arr2.length || carry) {
        let a = !arr1.length ? 0 : arr1.pop();
        let b = !arr2.length ? 0 : arr2.pop();
        let cur = a + b + carry;
        carry = Math.floor(cur / 10);
        cur %= 10;
        let curnode = new ListNode(cur);
        curnode.next = ans;
        ans = curnode;
    }
    return ans;
};
